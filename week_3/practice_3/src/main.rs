fn main() {
    let result = 10.00;     //f64 by default its used for float(decimals) and must have a decimal point 
    let interest:f32 = 8.35;      //f32 is single precision for smaller numbers
    let cost:f64 = 15000.600;     //f64 is double precision for larger numbers

    println!("result value is {}",result);
    println!("interest is {}",interest);
    println!("cost is {}",cost);
}
