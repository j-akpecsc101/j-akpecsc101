fn main() {
    let result = 10;        // i32 by default. the i means that its a signed integer and it can take both positive and negative values
    let age:u32 = 20;       // the u means that its an unsigned integer and can only take positive values
    let sum:i32 = 5-15;

    println!("Result value is {}",result);
    println!("Age is {}",age);
    println!("Sum is {}",sum);
}
