fn main() {
    // adition
    let sum = 5550 + 7310;
    println!("The sum of 5550 and 7310 = {}",sum);

    // subtraction
    let difference:f32 = 95.5 - 4.3;
    println!("The difference between 95.5 and 4.3 = {}",difference);

    // multiplication
    let product:i32 = 4*30;
    println!("The multiple of 4 and 30 ={}",product);

    // division
    let quotient:f32 = 56.7/32.2;
    println!("The quotient of 56.7 and 32.2 = {}",quotient);

    // remainder
    let remainder = 43 % 5;
    println!("The remainder of 43 and 5 = {}",remainder);    
}
